# Control structures are sequential, selection, iterative

# Sequential control structures:
print("Hello World")
var1 = "Welcome to Python"
print(var1)

# selection  Control Structures:
if True:
    print("Hello World")
else:
    print("Welcome to Python")

# Iterative Control Structures:
val = 0
while val<3:
    print("Welcome to Python")
    val += 1
print("Thanks")

