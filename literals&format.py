# Literals are Numaric and String literals.
# Numaric literals: ex.. 4, +5, -4.1...
# String literals: whit in the single code('') or double code("") or triple code (''' ''')
# Ex: "Hello", 'Hello', """ Hello """, ''' Hello '''

# format function:
# syntax of format
# format(value, specifiers)
print(8/9)                      # 0.8888888888888888
print(format(8/9,".3f"))        # 0.889
print("Hello")                  # Hello
print(format("Hello","<40"))    # 'Hello                     '
print(format("Hello", ">30"))   # '                 Hello'
print(format("Hello", "^30"))   # '         Hello        '


